public class Contact {
    private int mImgContacts;
    private String mNameContacts;
    private String mDescription;

    public int getmImgContacts() {
        return mImgContacts;
    }

    public void setmImgContacts(int mImgContacts) {
        this.mImgContacts = mImgContacts;
    }

    public String getmNameContacts() {
        return mNameContacts;
    }

    public void setmNameContacts(String mNameContacts) {
        this.mNameContacts = mNameContacts;
    }

    public String getmDescription() {
        return mDescription;
    }

    public void setmDescription(String mDescription) {
        this.mDescription = mDescription;
    }
}
